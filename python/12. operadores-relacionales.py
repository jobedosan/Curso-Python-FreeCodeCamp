#Operadores relacionales trabajando con números
print("Operadores relacionales trabajando con números")
print("Resultado de menor que")
print(5 < 10)
print(10 < 5)
print("\n")

print("Resultado de mayor que")
print(5 > 10)
print(10 > 5)
print("\n")

print("Resultado de mayor o igual que")
print(5 >= 10)
print(10 >= 5)
print(10 >= 10)

print("\n")
print("Resultado de menor o igual que")
print(5 <= 10)
print(10 <= 5)
print(10 <= 10)
print("\n")

print("Resultado de igual que")
print(5 == 10)
print(10 == 10)
print("\n")

print("Resultado de diferente que")
print(5 != 10)
print(10 != 10)
print("\n")


#Operador de suma como símbolo aritmético
print(5 + 5)
print(3.5 + 3.5)
print(-4 + 10)
print(-5 + -5)
print(-3.5 + -10)

#Operador de concatenación con el símbolo de suma
str_1 = "Hola"
str_2 = " "
str_3 = "Mundo"

print(str_1 + str_2 + str_3)
print("Hola" + " " + "Benjamin")

# Operador de resta
print(5 - 3)
print(3.5 - 2.5)
#Al restar números negativos estos se suman, como en las reglas matemáticas que conocemos
print(-4 - 10)
print(10 - 11)
print(10 - 11)
print(-3.5 - 10)

#Operador de multliplicación
print(5 * 6)
print(4 * 0)
print(-5 * -8)
print(-5 * 8)
print(4.5 * 7.9)
print(4.5 * -7.9)

#Operador de división
print(15 / 3)
print(15 / 8)
print(12.5 / 4)
print(4 / 4.5)
print(4.2 / 4.1)
print(4.5 / -7.9)
print(5 / 4)

#Operador de división entera
print(15 // 3)
print(15 // 8)

#Este operador nos muestra un resultado de coma flotante al utilizar operandos de coma flotante, sin embargo los decimales son truncados y el resultado dará 0 como parte decimal siempre
print(12.5 // 4)
print(4 // 4)
print(4.2 // 4.1)
print(4.2 // 4.1)
print(4 // 5)

#Operador de exponente o exponeciación
print(5 ** 3)
print(3 ** 8)
print(2 ** 3)

#Al hacer exponenciación con números de coma flotante, el resultado será un número de coma flotante
print(4.5 ** 4.7)
print(4 ** 4.7)

#Podemos obtener la raíz cuadrada elevando un número a 1/2, el resultado retornado será un número de coma flotante aunque el valor en la práctica común sea un número entero
print(81 ** (1/2))

#Cualquier número elevado a 0 será igual a 1
print(3 ** 0)

#Podemos obtener la raíz cúbica elevando un número a 1/3, el resultado retornado será un número de coma flotante aunque el valor en la práctica común sea un número entero
print(125 ** (1/3))

#Operador módulo
print(5 % 2)
print(4 % 3)
print(16 % 4)
print(16 % 6)
print(18 % 4)
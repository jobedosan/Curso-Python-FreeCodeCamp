#Datos numéricos (Números enteros)
print(type(10))
print(type(-15))
print(type(0))

#Datos numéricos (Números en coma flotante)
print(type(0.5))
print(type(0.0))
print(type(-4.7))

#Datos booleanos (True o False)
print(type(True))
print(type(False))

#Datos booleanos (True o False)
nombre = "Benjamin"
print(type(nombre))

#Comparación de números (como dato entero y como string)
print(type(5))
print(type("5"))

#Función len(), en este caso nos retornará 8 y 5, en una pasamos la variable definida anteriormente y en la siguiente la palabra perro
print(len(nombre))
print(len('perro'))

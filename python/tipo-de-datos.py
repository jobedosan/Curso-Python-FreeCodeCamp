#Datos numéricos (Números enteros)
print(type(10))
print(type(-15))
print(type(0))

#Datos numéricos (Números en coma flotante)
print(type(0.5))
print(type(0.0))
print(type(-4.7))

#Datos booleanos (True o False)
print(type(True))
print(type(False))

#Datos booleanos (True o False)
nombre = "Benjamin"
print(type(nombre))

#Datos string (cadena de caracteres)
print(type("Benjamin"))

#Comparación de números (como dato entero y como string)
print(type(5))
print(type("5"))

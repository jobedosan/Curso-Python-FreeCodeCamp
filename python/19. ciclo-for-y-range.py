# ciclo for que cuenta de cero a tres (un solo parámetro)
lista_ciclo_1 = []
for i in range(4):
    lista_ciclo_1.append(i)
    
print(lista_ciclo_1)
    
#Ciclo for que cuenta de 2 a seis (dos parámetros)
lista_ciclo_2 = []
for i in range(2, 7):
    lista_ciclo_2.append(i)
    
print(lista_ciclo_2)

#Ciclo for que cuenta de 0 a 50 de cinco en cinco (tres parámetros)
lista_ciclo_3 = []
for i in range(0, 51, 5):
    lista_ciclo_3.append(i)
    
print(lista_ciclo_3)

#Ciclo for que cuenta en reversa de 10 a 0 ()
lista_ciclo_4 = []
for i in range(10, -1, -1):
    lista_ciclo_4.append(i)
    
print(lista_ciclo_4)


